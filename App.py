        st.warning("OpenAI –Ω–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω (–Ω–µ—Ç –∫–ª—é—á–∞) ‚Äî –≥–µ–Ω–µ—Ä–∞—Ü–∏—è –æ—Ç—á—ë—Ç–∞ –Ω–µ–¥–æ—Å—Ç—É–ø–Ω–∞.")
