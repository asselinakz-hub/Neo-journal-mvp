DEFAULT_NAMES = ["–ê–º–µ—Ç–∏—Å—Ç","–ì—Ä–∞–Ω–∞—Ç","–¶–∏—Ç—Ä–∏–Ω","–°–∞–ø—Ñ–∏—Ä","–ì–µ–ª–∏–æ–¥–æ—Ä","–ò–∑—É–º—Ä—É–¥","–Ø–Ω—Ç–∞—Ä—å","–®—É–Ω–≥–∏—Ç","–†—É–±–∏–Ω"]

def _clean_pot_name(x: str) -> str:
    return norm_pot_name((x or "").strip(" \t\r\n-‚Äì‚Äî‚Ä¢*,:;").strip())

def parse_potentials_9(raw: str) -> List[str]:
    if not raw:
        return []
    s = raw.strip()

    # Try numbered 1..9
    numbered = []
    for i in range(1, 10):
        m = re.search(
            rf"(?:(?:^|\n|\s){i}\s*[\.\)]\s*)(.+?)(?=(?:\n|\s)(?:{i+1}\s*[\.\)]|$))",
            s, flags=re.S
        )
        if m:
            val = _clean_pot_name(m.group(1))
            if val:
                numbered.append(val)

    if len(numbered) >= 9:
        return numbered[:9]

    # Split lines/commas/semicolons/pipes
    s2 = re.sub(r"[\u2022‚Ä¢]", "\n", s)
    s2 = s2.replace(";", "\n").replace(",", "\n").replace("|", "\n")
    lines = [ln.strip() for ln in s2.splitlines() if ln.strip()]

    cleaned = []
    for ln in lines:
        ln = re.sub(r"^\d+\s*[\.\)]\s*", "", ln).strip()
        ln = ln.split("‚Äî")[0].split("-")[0].strip()
        if ln:
            cleaned.append(_clean_pot_name(ln))

    flat = [x for x in cleaned if x]

    # fill to 9
    existing = {x.lower() for x in flat}
    for name in DEFAULT_NAMES:
        if len(flat) >= 9:
            break
        if name.lower() not in existing:
            flat.append(name)
            existing.add(name.lower())

    return flat[:9]


